package snakeandladder;

public class Player {

	
	String name;
	int playerId;
	
	
	
	
	public Player(String name, int playerId) {
		super();
		this.name = name;
		this.playerId = playerId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPlayerId() {
		return playerId;
	}
	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}
	
	
	
	
	
}
